// ConsoleApplication1.cpp : Этот файл содержит функцию "main". Здесь начинается и заканчивается выполнение программы.
//
#include <iostream>
#include <Windows.h>
#include "module_1.h"
#include "module_2.h"
#include "module_3.h"

using namespace std;

int main()
{
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    int choice = 0;
    while (choice != 4)
    {
        cout << "\nМеню:\n";
        cout << "1 - Ввод данных\n";
        cout << "2 - Поиск учеников на скрипке\n";
        cout << "3 - Сохранение результата\n";
        cout << "4 - Выход\n";
        cout << "Выберите пункт: ";
        cin >> choice;

        switch (choice)
        {
        case 1:
            enter();
            break;
        case 2:
            find();
            break;
        case 3:
            save();
            break;
        case 4:
            cout << "Выход из программы...\n";
            break;
        default:
            cout << "Ошибка: введите число от 1 до 4\n";
        }
    }
    system("pause");
    return 0;
}

